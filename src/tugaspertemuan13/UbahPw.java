    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if (Tf_Hobi.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Isi hobi dulu!");
            return;
        }

        EntityManagerFactory emf = Persistence.createEntityManagerFactory("TugasPertemuan13PU");
        EntityManager em = emf.createEntityManager();
        em.getTransaction().begin();

        String user = Tf_User.getText();
        Pw y = em.find(Pw.class, user);

        if (y == null) {
            JOptionPane.showMessageDialog(null, "Error: User tiba-tiba tidak ditemukan!");
            return;
        }

        if (!Tf_Hobi.getText().equals(y.getHobi())) {
            JOptionPane.showMessageDialog(null, "Hobi tidak cocok!");
            return;
        }

        jLabelPassword3.setVisible(true);
        Tf_Password.setVisible(true);
        jButtonBuatPW.setVisible(true);

        Tf_Hobi.setEditable(false);
        Tf_Password.requestFocus();

        em.getTransaction().commit();
        em.close();
        emf.close();

    }//GEN-LAST:event_jButton2ActionPerformed

