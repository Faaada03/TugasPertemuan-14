    private void jButtonCariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCariActionPerformed
        // TODO add your handling code here:
        if (Tf_User.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Isi Terlebih Dahulu");
        } else {
            EntityManagerFactory emf = Persistence.createEntityManagerFactory("TugasPertemuan13PU");
            EntityManager em = emf.createEntityManager();

            em.getTransaction().begin();

            String user = Tf_User.getText();
            Pw y = em.find(Pw.class, user);

            if (y == null) {
                JOptionPane.showMessageDialog(null, "Username tidak ditemukan!");
                Tf_User.requestFocus();
            } else {
                jLabelHobi2.setVisible(true);
                Tf_Hobi.setVisible(true);
                jButton2.setVisible(true);

                Tf_User.setEditable(false);
                Tf_Hobi.requestFocus();
            }

            em.getTransaction().commit();
            em.close();
            emf.close();
        }
    }//GEN-LAST:event_jButtonCariActionPerformed

