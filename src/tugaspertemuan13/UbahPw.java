    private void jButtonBuatPWActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBuatPWActionPerformed
        // TODO add your handling code here:
        if (Tf_Password.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Cari Terlebih Dahulu");
        } else {
            EntityManagerFactory emf = Persistence.createEntityManagerFactory("DB_SwalayanPU");
            EntityManager em = emf.createEntityManager();
            em.getTransaction().begin();

            String user, pw;
            user = Tf_User.getText();
            pw = Tf_Password.getText();

            Pw x = em.find(Pw.class, user);
            x.setUsername(user);
            x.setPassword(pw);
            em.merge(x);

            em.getTransaction().commit();

            JOptionPane.showMessageDialog(null, "Berhasil diubah");

            em.close();
            emf.close();

            Login y = new Login();
            y.setVisible(true);
            this.dispose();
        }
    }//GEN-LAST:event_jButtonBuatPWActionPerformed

