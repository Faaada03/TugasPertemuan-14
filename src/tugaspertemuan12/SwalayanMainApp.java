    private void BtnClearAllActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnClearAllActionPerformed
        // TODO add your handling code here:                                          
        // Konfirmasi agar tidak salah klik
        int confirm = JOptionPane.showConfirmDialog(this,
                "Yakin ingin menghapus SEMUA data Stok Barang?",
                "Konfirmasi Hapus Semua",
                JOptionPane.YES_NO_OPTION);

        if (confirm != JOptionPane.YES_OPTION) {
            return;
        }

        EntityManager em = SwalayanMainApp.this.getEntityManager();
        em.getTransaction().begin();

        // 1. Hapus semua harga jual terlebih dahulu
        em.createQuery("DELETE FROM HargaJualBarang").executeUpdate();

        // 2. Hapus semua pembelian stok
        em.createQuery("DELETE FROM PembelianStokBarang").executeUpdate();

        em.getTransaction().commit();

        // Refresh tabel
        showTablePenjualan();
        showTable();

        JOptionPane.showMessageDialog(this, "Semua data berhasil dihapus!");
    }//GEN-LAST:event_BtnClearAllActionPerformed

    private void BtnClearAll2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnClearAll2ActionPerformed
        // TODO add your handling code here:
        int confirm = JOptionPane.showConfirmDialog(this,
                "Yakin ingin menghapus SEMUA data Penjualan Barang?",
                "Konfirmasi Hapus Semua",
                JOptionPane.YES_NO_OPTION);

        if (confirm != JOptionPane.YES_OPTION) {
            return;
        }

        DefaultTableModel model = (DefaultTableModel) Tabel_Penjualan_Barang.getModel();
        int rowCount = model.getRowCount();

        EntityManager em = SwalayanMainApp.this.getEntityManager();
        em.getTransaction().begin();

        for (int i = 0; i < rowCount; i++) {
            String id = model.getValueAt(i, 0).toString();
            HargaJualBarang data = em.find(HargaJualBarang.class, id);
            if (data != null) {
                em.remove(data);
            }
        }

        em.getTransaction().commit();

        showTablePenjualan();
        JOptionPane.showMessageDialog(this, "Semua data penjualan berhasil dihapus!");
    }//GEN-LAST:event_BtnClearAll2ActionPerformed
