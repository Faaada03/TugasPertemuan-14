    public void showTablePenjualan() {
        try {
            List<HargaJualBarang> dataPenjualan = getEntityManager()
                    .createQuery("SELECT h FROM HargaJualBarang h ORDER BY h.idBarang ASC", HargaJualBarang.class)
                    .getResultList();

            DefaultTableModel model = (DefaultTableModel) Tabel_Penjualan_Barang.getModel();
            model.setRowCount(0);

            for (HargaJualBarang h : dataPenjualan) {
                model.addRow(new Object[]{
                    h.getIdBarang(),
                    h.getBarang().getNamaBarang(),
                    h.getHargaJual(),
                    h.getRakBarang()
                });
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,
                    "Gagal menampilkan data Harga Jual: " + e.getMessage(),
                    "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

    private void showTable() {
        try {
            List<PembelianStokBarang> daftarStok = getEntityManager()
                    .createQuery("SELECT p FROM PembelianStokBarang p ORDER BY p.idBarang ASC", PembelianStokBarang.class)
                    .getResultList();

            DefaultTableModel model = (DefaultTableModel) Tabel_Stok_Barang.getModel();
            model.setRowCount(0);

            for (PembelianStokBarang stok : daftarStok) {
                model.addRow(new Object[]{
                    stok.getIdBarang(),
                    stok.getBarang().getNamaBarang(),
                    stok.getJumlahBeli(),
                    stok.getHargaBeli()
                });
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,
                    "Gagal menampilkan data: " + e.getMessage(),
                    "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

