    private void BtnCetakActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnCetakActionPerformed
        // TODO add your handling code here:
        try {
            // 1. Ambil data dari JPA
            EntityManager em = SwalayanMainApp.this.getEntityManager();
            List<PembelianStokBarang> data = em.createQuery(
                    "SELECT p FROM PembelianStokBarang p", PembelianStokBarang.class
            ).getResultList();

            if (data.isEmpty()) {
                JOptionPane.showMessageDialog(this,
                        "Tidak ada data untuk dicetak!",
                        "Informasi",
                        JOptionPane.INFORMATION_MESSAGE);
                return;
            }

            // 2. Isi data ke Jasper via Bean datasource
            JRBeanCollectionDataSource dataSource = new JRBeanCollectionDataSource(data);

            // 3. Parameter untuk judul & nama toko
            Map<String, Object> params = new HashMap<>();
            params.put("StoreName", "TOKO FAA MAJU JAYA ABADI");
            params.put("ReportTitle", "LAPORAN DATA PEMBELIAN / STOK BARANG");

            JasperReport report = JasperCompileManager.compileReport(
                    "src/tugaspertemuan12/ReportPembelianStok.jrxml"
            );

            // 5. Generate report
            JasperPrint print = JasperFillManager.fillReport(report, params, dataSource);

            // 6. Tampilkan viewer
            JasperViewer viewer = new JasperViewer(print, false);
            viewer.setTitle("Preview Laporan Stok Barang");
            viewer.setVisible(true);

        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this,
                    "Gagal mencetak laporan: " + e.getMessage(),
                    "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_BtnCetakActionPerformed

