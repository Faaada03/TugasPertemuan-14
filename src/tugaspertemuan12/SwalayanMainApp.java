    private void BtnDownloadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnDownloadActionPerformed
        // TODO add your handling code here:                                           

        JFileChooser chooser = new JFileChooser();
        chooser.setDialogTitle("Simpan CSV Data Pembelian");

        chooser.setSelectedFile(new File("data_pembelian_stok.csv"));

        if (chooser.showSaveDialog(this) != JFileChooser.APPROVE_OPTION) {
            return;
        }

        File file = chooser.getSelectedFile();

        try (Writer writer = new FileWriter(file)) {

            CSVFormat format = CSVFormat.DEFAULT.builder()
                    .setHeader("id_barang", "nama_barang", "jumlah_beli", "harga_beli")
                    .build();

            CSVPrinter csv = new CSVPrinter(writer, format);

            EntityManager em = getEntityManager();

            List<PembelianStokBarang> list = em.createQuery(
                    "SELECT p FROM PembelianStokBarang p", PembelianStokBarang.class
            ).getResultList();

            for (PembelianStokBarang p : list) {
                csv.printRecord(
                        p.getIdBarang(),
                        p.getNamaBarang(),
                        p.getJumlahBeli(),
                        p.getHargaBeli()
                );
            }

            JOptionPane.showMessageDialog(this,
                    "CSV berhasil didownload!",
                    "Sukses",
                    JOptionPane.INFORMATION_MESSAGE);

        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this,
                    "Gagal mendownload CSV: " + e.getMessage(),
                    "Error", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_BtnDownloadActionPerformed

    private void BtnUpload1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnUpload1ActionPerformed
        // TODO add your handling code here:                                              
        JFileChooser fc = new JFileChooser();
        fc.setDialogTitle("Pilih File CSV Harga Jual");

        if (fc.showOpenDialog(this) == JFileChooser.APPROVE_OPTION) {
            File file = fc.getSelectedFile();

            try (Reader reader = new FileReader(file)) {

                CSVFormat.Builder builder = CSVFormat.DEFAULT.builder();
                CSVFormat format = builder
                        .setHeader()
                        .setSkipHeaderRecord(true)
                        .build();

                Iterable<CSVRecord> records = format.parse(reader);

                EntityManager em = getEntityManager();
                em.getTransaction().begin();

                for (CSVRecord record : records) {

                    String idBarang = record.get("id_barang");
                    String namaBarang = record.get("nama_barang");
                    BigDecimal hargaJual = new BigDecimal(record.get("harga_jual"));
                    String rakBarang = record.get("rak_barang");

                    Barang b = em.find(Barang.class, idBarang);
                    if (b == null) {
                        b = new Barang();
                        b.setIdBarang(idBarang);
                        b.setNamaBarang(namaBarang);
                        em.persist(b);
                    }

                    HargaJualBarang h = em.find(HargaJualBarang.class, idBarang);

                    if (h == null) {
                        h = new HargaJualBarang();
                        h.setIdBarang(idBarang);

                    }
                    h.setBarang(b);
                    h.setHargaJual(hargaJual);
                    h.setRakBarang(rakBarang);
                    em.persist(h);
                }

                em.getTransaction().commit();
                JOptionPane.showMessageDialog(this,
                        "Import Harga Jual Barang Berhasil!",
                        "Sukses",
                        JOptionPane.INFORMATION_MESSAGE);

                showTablePenjualan();
                showTable();

            } catch (Exception e) {
                e.printStackTrace();
                JOptionPane.showMessageDialog(this,
                        "Gagal import CSV: " + e.getMessage(),
                        "Error", JOptionPane.ERROR_MESSAGE);
            }
        }

    }//GEN-LAST:event_BtnUpload1ActionPerformed

